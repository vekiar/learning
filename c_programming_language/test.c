// simple main function in C
int main()
{
  int x, y; // these are integers
  char a, b; // these are characters

  /* now we divide one char by the other
  if the results are x and y -> return x
  if the results are not     -> return y
  */

  if (a/b == x && b/a == y) {
    return x;
  } else {
    return 0;
  }
}
